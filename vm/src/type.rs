use crate::Tag;

/// A type in Scheme.
#[derive(Clone, Copy, Debug, Default, Eq, PartialEq)]
pub enum Type {
    /// A pair.
    #[default]
    Pair,
    /// A null.
    Null,
    /// A boolean.
    Boolean,
    /// A procedure.
    Procedure,
    /// A symbol.
    Symbol,
    /// A string.
    String,
    /// A character.
    Character,
    /// A vector.
    Vector,
    /// A byte vector.
    ByteVector,
    /// A record.
    Record,
    /// A foreign object
    Foreign = Tag::MAX as _,
}

// ── Cayley table ────────────────────────────────────────────────
//
// 32×32 algebraic dispatch table.  Every type dispatch decision is a
// single lookup: `CAYLEY[op][tag]`.
//
// Mapping to Stak's Type enum (for reference):
//   T_PAIR(12)  ↔  Type::Pair(0)
//   T_SYM(13)   ↔  Type::Symbol(4)
//   T_CLS(14)   ↔  Type::Procedure(3)
//   T_STR(15)   ↔  Type::String(5)
//   T_VEC(16)   ↔  Type::Vector(7)
//   T_CHAR(17)  ↔  Type::Character(6)
//
// The Cayley table uses its own u8 index space (0–31).

/// Cayley table dimension.
pub const N: usize = 32;

// ── Algebraic core (0–11) ───────────────────────────────────────

/// ⊤ — top / nil / '()
pub const TOP: u8 = 0;
/// ⊥ — bottom / #f / type error
pub const BOT: u8 = 1;
/// Quote / encoder
pub const Q: u8 = 2;
/// Eval / decoder
pub const E: u8 = 3;
/// First projection (car)
pub const CAR: u8 = 4;
/// Second projection (cdr = ρ ∘ cons)
pub const CDR: u8 = 5;
/// Pair constructor, core-preserving
pub const CONS: u8 = 6;
/// Branch (conditional dispatch)
pub const RHO: u8 = 7;
/// Explicit application
pub const APPLY: u8 = 8;
/// call/cc
pub const CC: u8 = 9;
/// Classifier (type predicate)
pub const TAU: u8 = 10;
/// Y combinator (fixed point)
pub const Y: u8 = 11;

// ── R4RS type tags (12–19) ──────────────────────────────────────

/// Pair type tag.
pub const T_PAIR: u8 = 12;
/// Symbol type tag.
pub const T_SYM: u8 = 13;
/// Closure / procedure type tag.
pub const T_CLS: u8 = 14;
/// String type tag.
pub const T_STR: u8 = 15;
/// Vector type tag.
pub const T_VEC: u8 = 16;
/// Character type tag.
pub const T_CHAR: u8 = 17;
/// First-class continuation type tag.
pub const T_CONT: u8 = 18;
/// Port type tag.
pub const T_PORT: u8 = 19;

// ── Special values (20–22) ──────────────────────────────────────

/// Boolean true.
pub const TRUE: u8 = 20;
/// End-of-file object.
pub const EOF: u8 = 21;
/// Void (unspecified).
pub const VOID: u8 = 22;

// ── Element lists ───────────────────────────────────────────────

/// The 10 core algebraic elements.
pub const CORE: [u8; 10] = [Q, E, CAR, CDR, CONS, RHO, APPLY, CC, TAU, Y];
/// The 8 R4RS type tags.
pub const TYPE_TAGS: [u8; 8] = [T_PAIR, T_SYM, T_CLS, T_STR, T_VEC, T_CHAR, T_CONT, T_PORT];

